function fmtLine(str, bd) { return str ? str.replace(/\{name\}/g, bd.name) : str; }
function fmtLineObj(obj, bd) { const out = {}; for (const [k, v] of Object.entries(obj)) out[k] = typeof v === 'string' ? fmtLine(v, bd) : v; return out; }
function randomEntry(arr) { return arr[Math.floor(Math.random() * arr.length)]; }

function getReactionPoolFor(bd) {
    if (bd.inner === 'widow') return 'widow';
    if (bd.inner === 'married' || bd.inner === 'married_child') return 'mature';
    if (bd.inner === 'unmarried' && bd.age >= 25) return 'mature';
    return 'shy';
}

function getReaction(bd) {
    var poolName = getReactionPoolFor(bd);
    var pool = poolName === 'widow' ? WIDOW_REACTIONS : poolName === 'mature' ? MATURE_REACTIONS : SHY_REACTIONS;
    return randomEntry(pool);
}

